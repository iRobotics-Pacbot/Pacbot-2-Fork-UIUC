const static std::array<uint32_t, 31> WALLS{
    0b00001111111111111111111111111111, 0b00001000000000000110000000000001,
    0b00001011110111110110111110111101, 0b00001011110111110110111110111101,
    0b00001011110111110110111110111101, 0b00001000000000000000000000000001,
    0b00001011110110111111110110111101, 0b00001011110110111111110110111101,
    0b00001000000110000110000110000001, 0b00001111110111110110111110111111,
    0b00001111110111110110111110111111, 0b00001111110110000000000110111111,
    0b00001111110110111111110110111111, 0b00001111110110111111110110111111,
    0b00001111110000111111110000111111, 0b00001111110110111111110110111111,
    0b00001111110110111111110110111111, 0b00001111110110000000000110111111,
    0b00001111110110111111110110111111, 0b00001111110110111111110110111111,
    0b00001000000000000110000000000001, 0b00001011110111110110111110111101,
    0b00001011110111110110111110111101, 0b00001000110000000000000000110001,
    0b00001110110110111111110110110111, 0b00001110110110111111110110110111,
    0b00001000000110000110000110000001, 0b00001011111111110110111111111101,
    0b00001011111111110110111111111101, 0b00001000000000000000000000000001,
    0b00001111111111111111111111111111};

bool wallAt(int row, int col) {
  if (row < 0 or row >= 31 or col < 0 or col >= 28) {
    return true;
  }
  return (WALLS[row] >> col) & 1;
}
